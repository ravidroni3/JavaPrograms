package Loops;

public class SwitchEx {

	public static void main(String[] args) {
		int x = 10;
		switch (x) {
		case 0:
			System.out.println("a is o");
			break;
		case 3:
			System.out.println("a is 3");
			break;
		case 7:
			System.out.println("a is 7");
			break;
		case 9:
			System.out.println("a is 9");
			break;
		default : System.out.println("case not found");

		}

	}

}
