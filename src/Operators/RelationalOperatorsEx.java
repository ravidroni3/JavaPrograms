package Operators;

public class RelationalOperatorsEx {

	public static void main(String[] args) {
		byte x = 4;
		byte y = 6;
		System.out.println("The result of x>y is: "+(x>y));
		System.out.println("The result of x>=y is: "+(x>=y));
		System.out.println("The result of x<y is: "+(x<y));
		System.out.println("The result of x<=y is: "+(x<=y));
		System.out.println("The result of x==y is: "+(x==y));
		System.out.println("The result of !x==y is: " + !(x == y));

	}

}
